import { Bot, webhookCallback } from 'grammy'
import { BOT_TOKEN } from './auth'
import { APP_URL } from './env'

export const bot = BOT_TOKEN ? new Bot(BOT_TOKEN) : null

const OPEN = 'Играть 🎲'

export function appKeyboard(startParam?: string) {
  if (!APP_URL) return undefined
  const url = startParam ? `${APP_URL}?startapp=${encodeURIComponent(startParam)}` : APP_URL
  return { inline_keyboard: [[{ text: OPEN, web_app: { url } }]] }
}

if (bot) {
  void bot.api.setMyDescription(
    'Магнат это уютная настолка про недвижимость в духе «Монополии», только своя.\n\n' +
      'Бросай кубики, скупай улицы и вокзалы, собирай цветные группы в монополии и застраивай их домами. ' +
      'Соперники платят тебе аренду, а ты стараешься не разориться сам. Кто останется единственным платёжеспособным магнатом, тот и победил.\n\n' +
      'Можно сыграть с ботами в одиночку на трёх уровнях сложности или позвать друзей в общую комнату по коду из четырёх символов.\n\n' +
      'Заходи за стол, когда захочется скоротать вечер.',
  ).catch(() => {})
  void bot.api.setMyShortDescription('Уютная настолка про недвижимость. Скупай улицы, строй дома, разоряй соперников 🎲').catch(() => {})

  // стандартное меню команд (список, всплывающий по «/»)
  void bot.api.setMyCommands([
    { command: 'start', description: 'Открыть игру' },
    { command: 'play', description: 'Сыграть партию' },
    { command: 'rules', description: 'Как играть' },
    { command: 'about', description: 'Об игре' },
    { command: 'help', description: 'Помощь' },
  ]).catch(() => {})

  bot.command('start', async ctx => {
    const payload = ctx.match?.toString() ?? ''
    // диплинк комнаты: /start room_ABCD открывает игру сразу в этой комнате
    const startParam = payload.startsWith('room_') ? payload : undefined
    const name = ctx.from?.first_name || 'друг'
    await ctx.reply(
      `Привет, ${name}! Это Магнат 🎲\n\n` +
        'Если совсем просто, это уютная настолка про недвижимость, чуть похожая на «Монополию». ' +
        'Бросаешь кубики, ходишь по кругу и скупаешь улицы, вокзалы и предприятия.\n\n' +
        'Собрал всю цветную группу, получил монополию: аренда растёт, а с домами и отелями соперники платят тебе по-крупному. ' +
        'Побеждает тот, кто оставит остальных без денег и станет единственным магнатом за столом.\n\n' +
        'Ничего сложного и никакой спешки: можно сыграть с ботами в одиночку, а можно позвать друзей в общую комнату по коду. Нажми кнопку «Играть» внизу и заходи за стол. Жду тебя! 🎲',
      { reply_markup: appKeyboard(startParam) },
    )
  })

  bot.command('play', async ctx => {
    await ctx.reply('Раскладываю поле 🎲 Заходи за стол!', { reply_markup: appKeyboard() })
  })

  bot.command('rules', async ctx => {
    await ctx.reply(
      'Правила Магната 🎲\n\n' +
        'Цель: остаться единственным платёжеспособным игроком за столом.\n\n' +
        '• В свой ход бросай кубики и двигайся по кругу.\n' +
        '• Встал на ничью улицу, вокзал или предприятие, можешь купить.\n' +
        '• Встал на чужое, платишь владельцу аренду.\n' +
        '• Прошёл «Старт», получаешь зарплату 2000.\n\n' +
        'Монополии и дома:\n' +
        '• Собрал всю цветную группу, аренда сразу удваивается.\n' +
        '• На полной группе можно строить дома и отель, аренда растёт в разы.\n\n' +
        'Особые клетки:\n' +
        '• Шанс и Казна, тянешь карту с событием.\n' +
        '• Налог, платишь в банк.\n' +
        '• На нары, отправляешься в тюрьму. Три дубля подряд тоже.\n\n' +
        'Не хватает денег на выплату, банк сам продаёт твои дома и закладывает участки. Совсем нечем платить, ты банкрот.',
      { reply_markup: appKeyboard() },
    )
  })

  bot.command('about', async ctx => {
    await ctx.reply(
      'Магнат 🎲\n\n' +
        'Тёплая настолка про недвижимость в духе «Монополии», только своя: ' +
        'со своим полем, своими улицами и уютным столом под лампой.\n\n' +
        'Как можно играть:\n' +
        '• Одиночная игра против ботов, три уровня сложности.\n' +
        '• Быстрая игра, случайные соперники онлайн.\n' +
        '• С друзьями, общая комната по коду из четырёх символов.\n\n' +
        'Без спешки и без нервов, на пару партий за вечер. Заходи 💛',
      { reply_markup: appKeyboard() },
    )
  })

  bot.command('help', async ctx => {
    await ctx.reply(
      'Магнат 🎲 настолка про недвижимость.\n\n' +
        'Команды:\n' +
        '/start открыть игру\n' +
        '/play сыграть партию\n' +
        '/rules как играть\n' +
        '/about об игре\n' +
        '/help это сообщение\n\n' +
        'Жми кнопку ниже, чтобы играть 👇',
      { reply_markup: appKeyboard() },
    )
  })
}

export const botWebhook = bot ? webhookCallback(bot, 'hono') : null
